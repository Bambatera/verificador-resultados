    private void btnExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExecutarActionPerformed
        if (!this.txtArquivo.getText().trim().isBlank()) {
            String filePath = this.txtArquivo.getText().trim();
            try {
                ExcelPatternAnalysis epa = new ExcelPatternAnalysis();
                List<Concurso> concursos = epa.readExcelFile(filePath);
                System.out.println(concursos);
                if (chkAnalisar.isSelected()) {
                    epa.startAnalisys(concursos);
                }
                if (chkCalcular.isSelected()) {
                    ProbabilityCalculation pc = new ProbabilityCalculation();
                    pc.startProbabilityCalculation(concursos);
                }
            } catch (Exception ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_btnExecutarActionPerformed

